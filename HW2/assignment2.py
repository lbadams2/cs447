THRESHOLD = 3
MAX_LEN = 60
class TextDataset(data.Dataset):
  def __init__(self, examples, split, ixtoword=None, wordtoix=None, THRESHOLD=THRESHOLD):
    self.examples = examples
    self.split = split
    self.THRESHOLD = THRESHOLD
    self.textual_ids = []
    self.ixtoword = ixtoword
    self.wordtoix = wordtoix
    self.build_dictionary()
    ### TO-DO
  
  def build_dictionary(self):
    ### TO-DO
    ### <end> should be at idx 0
    ### <unk> should be at idx 1
    if not self.wordtoix:
        self.wordtoix = {}
        self.ixtoword = {}
        word_counts = {}
        self.ixtoword[0] = '<end>'
        self.ixtoword[1] = '<unk>'
        self.wordtoix['<end>'] = 0
        self.wordtoix['<unk>'] = 1
        for ex in self.examples:
            lower_ex = [x.lower() for x in ex.text]
            for w in lower_ex:
                if w not in word_counts:
                    word_counts[w] = 1
                else:
                    word_counts[w] += 1
        index = 2
        for w in word_counts:
            if w != '<end>' and w != '<unk>' and word_counts[w] >= self.THRESHOLD:
                self.ixtoword[index] = w
                self.wordtoix[w] = index
                index += 1
    
    for ex in self.examples:
        lower_ex = [x.lower() for x in ex.text]
        id_list = []
        for word in lower_ex:
            if word in self.wordtoix:
                id_list.append(self.wordtoix[word])
            else:
                id_list.append(self.wordtoix['<unk>'])
        self.textual_ids.append(id_list)
    return self.textual_ids, self.ixtoword, self.wordtoix
  
  def get_label(self, index):
    ### TO-DO
    label = self.examples[index].label.strip()
    if label == 'positive':
      return 0
    else:
      return 1
   
  def get_text(self, index):
    ### TO-DO
    id_sen = self.textual_ids[index]
    new_id_sen = id_sen.copy()
    while len(new_id_sen) < MAX_LEN:
        new_id_sen.append(0)
    return new_id_sen
    
  
  def __len__(self):
    ### TO-DO
    return len(self.examples)
  
  def __getitem__(self, index):
    ### TO-DO
    sen_ids = self.get_text(index)
    lbl = self.get_label(index)
    text_len = len(self.textual_ids[index])
    idx_tensor = torch.LongTensor(sen_ids)
    return idx_tensor, text_len, lbl

